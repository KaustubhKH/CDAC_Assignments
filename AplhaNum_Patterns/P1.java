public class P1
{
	public static void main(String args[])
	{
		System.out.println();
		
		//used for inside for to create a pattern...
		//nested for
		for(int i=1 ; i<=5 ; i++){
			for(int a=1 ; a<=i ; a++){
				System.out.print(a+" ");
			}
			System.out.println();
		}
		System.out.println();
	}
}
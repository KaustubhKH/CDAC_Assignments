public class P3
{
	public static void main(String args[])
	{
		
		//used for inside for to create a pattern...
		//nested for
		
		System.out.println();
		for(int i=1 ; i<=5 ; i++){
			for(int a=1 ; a<=i ; a++){
				System.out.print("* ");//printing the stars..
			}
			System.out.println();
		}
		System.out.println();
	}
}